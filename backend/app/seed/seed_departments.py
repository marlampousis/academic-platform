def seed_departments(db: Session) -> dict:
    created = 0
    skipped = 0
    missing_institutions: list[str] = []

    for department_data in DEPARTMENTS:
        institution_name_el = department_data["institution_name_el"]

        institution = (
            db.query(Institution)
            .filter(Institution.name_el == institution_name_el)
            .first()
        )

        if not institution:
            if institution_name_el not in missing_institutions:
                missing_institutions.append(institution_name_el)
            continue

        existing_department = (
            db.query(Department)
            .filter(
                Department.institution_id == institution.id,
                Department.name_el == department_data["name_el"],
            )
            .first()
        )

        if existing_department:
            skipped += 1
            continue

        department = Department(
            institution_id=institution.id,
            name_el=department_data["name_el"],
            name_en=department_data["name_en"],
            department_type=department_data["department_type"],
            city=department_data["city"],
            website_url=department_data["website_url"],
        )

        db.add(department)
        created += 1

    try:
        db.commit()
    except Exception:
        db.rollback()
        raise

    return {
        "created": created,
        "skipped": skipped,
        "missing_institutions": missing_institutions,
        "dataset_records": len(DEPARTMENTS),
    }


def run_departments_seeder() -> None:
    db = SessionLocal()

    try:
        result = seed_departments(db)

        print("Departments seeder completed.")
        print(f"Dataset records: {result['dataset_records']}")
        print(f"Created: {result['created']}")
        print(f"Skipped: {result['skipped']}")

        if result["missing_institutions"]:
            print("Missing institutions:")

            for institution_name in result["missing_institutions"]:
                print(f"- {institution_name}")

    except Exception as exc:
        print(f"Departments seeder failed: {exc}")
        raise

    finally:
        db.close()


if __name__ == "__main__":
    run_departments_seeder()
